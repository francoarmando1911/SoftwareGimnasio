    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String usuario = txtUsuario.getText();
        String contrasena = new String(txtContrasena.getPassword());
        Usuario usr = control.validarUsuario(usuario, contrasena);

        if (usr != null) {
            Rol rol = usr.getRol();

            if (rol != null) {
                String nombreRol = rol.getNombreRol();

                if (nombreRol.equals("coach")) {
                    PrincipalCoach pCoach = new PrincipalCoach(control, usr);
                    pCoach.setVisible(true);
                    pCoach.setLocationRelativeTo(null);
                    this.dispose();
                } else if (nombreRol.equals("admin")) {
                    PrincipalAdmin pAdmin = new PrincipalAdmin(control, usr);
                    pAdmin.setVisible(true);
                    pAdmin.setLocationRelativeTo(null);
                    this.dispose();
                } else {
                    jTextArea1.setText("Rol desconocido. Contacte al administrador.");
                }
            } else {
                jTextArea1.setText("El usuario no tiene un rol asignado.");
            }
        } else {
            jTextArea1.setText("Usuario o contrase√±a incorrectos.");
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

